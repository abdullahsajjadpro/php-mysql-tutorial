<?php

/**
 * Expressions:-
 *  - The combination of Operands with an operator to produce a result is called an expression.
 */

// Example:-
$num1 = 10; // $num1 is a Operand.
$num2 = 20; // $num2 is another Operand.
$sum = $num1 + $num2; // Plus is a Operator. This is an Expression because a operation of Plus will happen.

// Printing in the Server:-
echo "The Sum of " . $num1 . " and " . $num2 . " is " . $sum;
// Ouput is:- The Sum of 10 and 20 is 30
